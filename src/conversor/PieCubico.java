package conversor;

public class PieCubico {
	
	public double aCentimetroCubico(double cantidad) {
		return cantidad * 28316.8;
	}
	
	public double aMetroCubico(double cantidad){
		return cantidad*0.0283168;
	}
	
	public double aMilimetroCubico(double cantidad){
		return cantidad*2.832e+7;
	}
		
	public double aKilometroCubico(double cantidad){
		return cantidad*2.83168e-11;
	}
	
	public double aPulgadaCubico(double cantidad){
		return cantidad*1728;
	}

}

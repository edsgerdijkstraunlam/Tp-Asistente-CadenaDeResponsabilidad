package conversor;

public class Onza {
	
	public double aKilo(double cantidad) {
		return cantidad * 0.0283495;
	}
	
	public double aGramo(double cantidad) {
		return cantidad * 28.3495;
	}
	
	public double aMiligramo(double cantidad) {
		return cantidad * 28349.5;
	}
	
	public double aTonelada(double cantidad) {
		return cantidad * 2.83495e-5;
	}

}

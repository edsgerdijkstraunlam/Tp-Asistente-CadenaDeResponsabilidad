package edsger_dijkstra_unlam.asistente.conversor;

public class CentimetroCubico {
	
	public double aMetroCubico(double cantidad){
		return cantidad * 1e-6;
	}
	
	public double aMilimetroCubico(double cantidad){
		return cantidad*1000;
	}
		
	public double aKilometroCubico(double cantidad){
		return cantidad*1e-15;
	}
	
	public double aPulgadaCubico(double cantidad){
		return cantidad*0.0610237;
	}
	
	public double aPieCubico(double cantidad){
		return cantidad*3.53147e-5;
	}

}
